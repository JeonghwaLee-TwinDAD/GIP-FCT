/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  // Here we set the TIM6 interrupt priority to 1, so that the systick,
  // which is handled internally by the freertos and whose interrupt is set to 0,
  // can have a higher priority, otherwise the freertos won't work correctly
  while (HAL_InitTick(3) != HAL_OK) {}

  // We add this call here because it's needed for the passwthrough
  MX_GPIO_Init();

  if(STM32_GetBootMode() == STM32_BOOT_PASSTHROUGH) {
    ST8500_assert_reset();

    HAL_GPIO_WritePin(LED0_GPIO_Port, LED0_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin, GPIO_PIN_SET);

    if (ST8500_GetBootMode() == ST8500_BOOT_FROM_UART) {
      HAL_GPIO_WritePin(ST8500_BOOT_W_GPIO_Port, ST8500_BOOT_W_Pin, GPIO_PIN_RESET);
    } else {
      HAL_GPIO_WritePin(ST8500_BOOT_W_GPIO_Port, ST8500_BOOT_W_Pin, GPIO_PIN_SET);
    }

    STM32_pass_through_activate();

    HAL_GPIO_TogglePin(LED3_GPIO_Port, LED3_Pin);

    ST8500_release_reset();

    while (1) {
      __WFI();
    }
  }

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();

  /* Initialize interrupts */
  MX_NVIC_Init();
  /* USER CODE BEGIN 2 */

  HAL_GPIO_WritePin(LED0_GPIO_Port, LED0_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin, GPIO_PIN_SET);

  // put ST8500 under reset and in the meantime check the STM32 mode
  ST8500_assert_reset();

  /* USER CODE END 2 */

  /* Init scheduler */
  osKernelInitialize();  /* Call init function for freertos objects (in freertos.c) */
  MX_FREERTOS_Init();
  /* Start scheduler */
  osKernelStart();

  /* We should never get here as control is now taken by the scheduler */
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

